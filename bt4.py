def tinh_trung_binh_cong(a, b, c):
    """
    Hàm tính trung bình cộng của ba số a, b, c.
    Công thức: (a + b + c) / 3
    """
    return (a + b + c) / 3

# Ví dụ sử dụng:
print(tinh_trung_binh_cong(3, 6, 9))   # 6.0
print(tinh_trung_binh_cong(10, 20, 30)) # 20.0
print(tinh_trung_binh_cong(1, 2, 3))    # 2.0